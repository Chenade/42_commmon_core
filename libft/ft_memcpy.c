#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (!dest && !src)
		return (dest);
	i = 0;
	while (i < n)
	{
		*(unsigned char *)(dest + i) = *(unsigned const char *)(src + i);
		i += 1;
	}
	return (dest);
}
